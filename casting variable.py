#variable casting

print("casting integer to string")
integer_number = 5
integer_number_casted_to_string = str(integer_number)
print(type(integer_number_casted_to_string))
print(integer_number_casted_to_string)

#float number casting integer to float
print("casting integer to float")
integer_number_casted_to_float = float(integer_number)
print(type(integer_number_casted_to_float))
print(integer_number_casted_to_float)

#float to integer
print("casting float to integer")
float_casted_to_integer = int(integer_number_casted_to_float)
print(type(float_casted_to_integer))
print(float_casted_to_integer)